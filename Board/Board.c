void Board_init(){
    // Initialize system clock to 120 MHz
    ASSERT( SysCtlClockFreqSet(
                               (SYSCTL_XTAL_25MHZ | SYSCTL_OSC_MAIN |
                                SYSCTL_USE_PLL | SYSCTL_CFG_VCO_480),
                               SYSTEM_CLOCK) == SYSTEM_CLOCK);
}

